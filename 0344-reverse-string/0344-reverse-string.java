class Solution {
    public void reverseString(char[] s) {
        int n=s.length;
        for(int i=0;i<n;i++){
            if(i<=n){
                char temp=s[i];
                s[i]=s[n-1];
                s[n-1]=temp;
                n--;
                
            }
            
        }
       

    
        
       


    }
}